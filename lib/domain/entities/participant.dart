class Participant {
  final String bib;
  final String name;
  final String age;
  final String gender;
  final bool isCompleted;
  final int duration;
  final double distance;
  final String globalStartTime;

  Participant({
    required this.bib,
    required this.name,
    required this.age,
    required this.gender,
    required this.isCompleted,
    required this.duration,
    required this.distance,
    required this.globalStartTime,
  });

}
